package by.bia.labAssist.util;

public class ResultTableRow {

    private String cipher;
    private String sampleName;
    private String elementNames;
    private String elementNorms;
    private String elementResults;

    public String getCipher() {
        return cipher;
    }

    public void setCipher(String cipher) {
        this.cipher = cipher;
    }

    public String getSampleName() {
        return sampleName;
    }

    public void setSampleName(String sampleName) {
        this.sampleName = sampleName;
    }

    public String getElementNames() {
        return elementNames;
    }

    public void setElementNames(String elementNames) {
        this.elementNames = elementNames;
    }

    public String getElementNorms() {
        return elementNorms;
    }

    public void setElementNorms(String elementNorms) {
        this.elementNorms = elementNorms;
    }

    public String getElementResults() {
        return elementResults;
    }

    public void setElementResults(String elementResults) {
        this.elementResults = elementResults;
    }
}
